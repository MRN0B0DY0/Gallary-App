class Gallery extends Component {
  state = {
    displayimgurl:
      'https://assets.ccbp.in/frontend/react-js/nature-mountain-with-pond-img.png',
    displayimgalttext: 'nature mountain with pond',
    activeID: 0,
  }

  thumbnailClick = (id, imageUrl, imageAltText) => {
    this.setState({displayimgurl: imageUrl})
    this.setState({displayimgalttext: imageAltText})
    this.setState({activeID: id})
  }

  render() {
    const {displayimgalttext, displayimgurl} = this.state
    let {activeID} = this.state

    return (
      <div>
        <div className="gallery-container">
          <div>
            <img
              src={displayimgurl}
              alt={displayimgalttext}
              className="main-image"
            />
          </div>
          <div className="gallery-details">
            <h1 className="gallery-title">Nature Photography</h1>
            <p className="gallery-subtitle">Nature Photography by Rahul</p>
            <ul className="thumbnail-list">
              {imagesList.map(eachItem => (
                <ThumbnailItem
                  thumbnaillist={eachItem}
                  thumbnailClick={this.thumbnailClick}
                  key={eachItem.id}
                  isActivate={activeID === eachItem.id}
                />
              ))}
            </ul>
          </div>
        </div>
      </div>
    )
  }
}

export default Gallery
